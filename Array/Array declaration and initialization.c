
//! การประกาศอาร์เรย์และการกำหนดค่าเริ่มต้น

#include <stdio.h>

int main() {
    // ประกาศอาร์เรย์ของจำนวนเต็มที่มีขนาด 5
    int numbers[5] = {1, 2, 3, 4, 5};

    // เข้าถึงและพิมพ์สมาชิกของอาร์เรย์
    for (int i = 0; i < 5; i++) {
        printf("%d ", numbers[i]);
    }
    printf("\n");

    return 0;
}
